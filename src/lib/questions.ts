export function getQuestionsByYear(): Record<number, Question[]> {
  const grouped: Record<number, Question[]> = {};
  dseQuestions.forEach(q => {
    if (!grouped[q.year]) {
      grouped[q.year] = [];
    }
    grouped[q.year].push(q);
  });
  return grouped;
}

